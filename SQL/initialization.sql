CREATE OR REPLACE FUNCTION set_hotel_manager()
RETURNS TRIGGER AS $$
BEGIN 
	IF NEW.jobPosition = 'Manager' THEN
		UPDATE Hotel
		SET managerID = NEW.SSN
		WHERE address = NEW.hotelID;
	END IF;
	RETURN NEW;
END;
$$ LANGUAGE plpgsql;

