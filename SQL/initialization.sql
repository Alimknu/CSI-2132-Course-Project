ADD FOREIGN KEY (managerID) REFERENCES Employee (SSN) ON DELETE SET NULL;

ALTER TABLE Employee
ADD FOREIGN KEY (hotelID) REFERENCES Hotel (address) ON DELETE SET NULL;

